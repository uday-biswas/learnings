#operators in python

#arithmetic operators
# +, -, *, /, %, //, **
print(10 + 20)
print(10 - 20)
print(10 * 20)
print(10 / 20)
print(10 % 20)
print(10 // 20)  #floor division
print(10 ** 2)  #exponentiation 

#relational operators
# >, <, >=, <=, ==, !=
print(10 > 20)
print(10 < 20)
print(10 >= 20)
print(10 <= 20)
print(10 == 20)
print(10 != 20)
 
#logical operators
# and, or, not
print(10 > 20 and 20 < 30)
print(10 > 20 or 20 < 30)
print(not(10 > 20))